package Week_03_01;

public class Hometask01 {

	public static void main(String[] args) {
		int x = 63;
		int y = 18;
		int z = 52;
		int q = 60;
		int sum = x + y + z + q;
		
		System.out.println("x값은 " + x + "입니다.");
		System.out.println("y값은 " + y + "입니다.");
		System.out.println("z값은 " + z + "입니다.");
		System.out.println("q값은 " + q + "입니다.");
		System.out.println("합계는 " + sum + "입니다.");
		System.out.println("평균은 " + sum / 4 + "입니다.");
		System.out.println("합계는 " + (x + y + z + q) + "입니다.");
		System.out.println("평균은 " + (x + y + z + q) / 4 + "입니다.");
	}

}
