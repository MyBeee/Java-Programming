package Week_03_01;

public class Hometask08 {

	public static void main(String[] args) {
		double cir_p = 3.14;
		double cir_r = 5;
		double cir_s = cir_p * cir_r * cir_r;
		double cir_v = 4 * cir_r * cir_r * cir_r / 3;
		
		System.out.println("원의 반지름이 " + cir_r + "일 떄, 원의 넓이는 " + cir_s + "이고 원의 부피는 " + cir_v + "이다.");
	}

}
